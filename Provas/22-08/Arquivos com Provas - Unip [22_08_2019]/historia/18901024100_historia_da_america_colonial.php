                                    <h4>Questões de múltipla escolha </h4>

                                    <p>Disciplina: 661040 - História da América Colonial</p>

                                    <p><strong>Questão 1:</strong> Um dos problemas essenciais para a análise histórica é a necessidade de fontes. Para o estudo específico da América pré-colombiana, a questão é ainda mais significativa, já que boa parte dos documentos estava relacionada a sacerdotes e, eventualmente, a assuntos religiosos -de modo que parte significativa foi destruída pelos europeus. Como, então, é possível progredir nos estudos históricoscontemporâneos acerca dessa temática? Assinale a alternativa que nãoresponde a essa questão:</br>
                                    </br>

                                    A) Há de se ter em vista que o tema já está bastante esgotado. Temos um bom conhecimento desseperíodo e não é possível avançar significativamente.</br>

                                    B) Os achados arqueológicos geram importantes análises de diversos aspectos da vida pré-colombiana, em especial das características urbanas e do cotidiano.</br>

                                    C) A parceria com outras ciências, como a antropologia e a sociologia, é capaz de investigar questões complementares e que auxiliam o avanço dos estudos.</br>

                                    D) É possível procurar “a voz dos vencidos” nos relatos orais contemporâneos e dos mestiços doperíodo histórico em questão e, através de uma análise completa, angariar maiores características.</br>

                                    E) Não se deve deixar de ler tudo o que já foi produzido e, a partir daí, com a descoberta da possibilidade de diferentes enfoques, voltar aos documentos e, cotejando com o que possa ainda ser descortinado, comonovas fontes, promover importantes constatações.</br>
                                    </br>

                                    </p>

                                    <p><strong>Questão 2:</strong> A Holanda teve relação direta com o processo de colonização da Época Moderna ao mesmo tempo em que conquistou sua independência -após um significativo período de luta. Como foi a atuação central dos holandeses nas relações coloniais?</br>
                                    </br>

                                    A) Foram os que mais adquiriram territórios, sobretudo após seus fortes ataques aos espanhóis.</br>

                                    B) Souberam garantir os principais entrepostos do tráfico negreiro. Com isso, já em meados do século XVI, passaram a ser preponderantes e, praticamente, monopolizaram o fornecimento desse tipo de mão de obra para a América. No século XVII, inclusive, nenhum outro país conseguia mais participar do trato direto do tráfico, ou seja, com os próprios africanos.</br>

                                    C) Estabeleceram-se em torno do trato comercial, tendo como um dos exemplos mais significativos a parceria do açúcar até onde foi possível. A partir daí, atacaram e conquistaram territórios para a manutenção deseu sistema econômico.</br>

                                    D) Investiram em um forte desenvolvimento industrial e, a partir daí, conseguiram se especializar em alguns produtos, como a indústria têxtil, e garantir um enorme mercado -inclusive atingindo uma revolução industrial.</br>

                                    E) Dedicaram-se à indústria naval e, com ela, após a vitória sobre a Espanha com sua Invencível Armada, em 1588, passaram a dominar todo o mercado mundial sem nenhum concorrente direto, tendo os portugueses como parceiros.</br>
                                    
                                    </br>
                                    </p>

                                    <p><strong>Questão 3:</strong> Os maias tiveram presença muito significativa na Península de Yucatán e em parte da América Central.  Suas invenções influenciam a vida das pessoas nessas regiões até os dias atuais, notadamente na agricultura e construções. Além disso as cidades tinham uma vida bastante dedicada à busca do favor dos deuses. Qual era a estrutura política base dos maias que os diferenciou de outros povos como os astecas e os incas?</br>
                                    </br>

                                    A) Formavam um grande império conquistador, militarista, que não tinha nenhuma divisão internade poder.</br>

                                    B) Desenvolveram um governo militar e absolutista nos moldes europeus da época moderna, mantido pela força da conquista de outros povos.</br>

                                    C) Baseados em pequenas comunidades, não chegaram a formar cidades. Ficavam baseados em laços familiares, em diminutos grupos locais.</br>

                                    D) A base central de poder eram cidades-estado, nas quais o controle central de poder era hereditário e teocrático.</br>

                                    E) O poder político poderia ser razoavelmente comparado ao de uma democracia contemporânea, pois envolvia a participação de quase todos os habitantes locais.</br>
                                    </br>

                                    </p>

                                    <p><strong>Questão 4:</strong> Os espanhóis, após terem se maravilhado com os gigantescos lucros obtidos com as conquistas, passaram a perceber que, dentro da lógica mercantilista, era possível promover um sistema exploratório capaz de garantir a exploração do ouro e da prata. A perspectiva era toda baseada no controle Real, apesar de promover uma divisão geral que permitia um grande domínio de funcionários cuidadosamente escolhidos -que se tornavam a maior autoridade executiva do Novo Mundo. Essa repartição foi formada por quatro áreas centrais de colonização que se estruturaram no decorrer da colonização e por séculos. Qual era essa instituição? </br>
                                    </br>

                                    A) Os Vice-Reinos.</br>

                                    B) As Capitanias Hereditárias.</br>

                                    C) As Colônias.</br>

                                    D) Os Conselhos das Índias.</br>

                                    E) As Casas de Contratação. </br>
                                    </br>

                                    </p>

                                    <p><strong>Questão 5:</strong> Apesar de a estrutura colonial espanhola estar diretamente relacionada ao poder domonarca e de suas determinações, uma instância de justiça local, com certa autonomia e formada pela elite local, também se estabeleceu, chegando até mesmo a selecionar uma autoridade política e judiciária -o alcaide -como seu grande representante. Sua importância foi crescendo à medida que essas elites ganharam maior vigor econômico, e seus interesses foram se tornando diferentes dos da Metrópole. Qual era essa instituição?</br>
                                    </br>

                                    A) Capitanias Hereditárias.</br>

                                    B) Ayllus.</br>

                                    C) Tribunais de Justiça.</br>

                                    D) Cabildos.</br>

                                    E) Vice-reinos.</br>
                                    </br>

                                    </p>

                                    <p><strong>Questão 6:</strong> Os Tratados de Rivalidade foram desenvolvidos entre a Espanha e Portugal para assegurar a posse dos territórios que estavam sendo alcançados. De um lado, a Espanha desejava garantir suas descobertas do Oeste (a partir da viagem de Colombo em 1492); de outro lado, Portugal pretendia afirmar a posse da África -de sua rota litorânea já iniciada em 1415. O primeiro desses tratados, já no bojo da descoberta de Colombo, foi a Bula Inter Coetera, de 1493, que traçava um meridiano de 100 léguas a oeste de Cabo Verde -a Espanha ficava com o Oeste (Ocidente) e Portugal, por sua vez, com o Leste (Oriente). O reino português, contudo, não aceitou tal divisão. O que resultou dessa não aceitação portuguesa?</br>
                                    </br>

                                    A) Após ameaças de guerra, foi decidido que os demais países da Europa também deveriam ter direito às terras e rotas. </br>

                                    B) Foi estabelecida uma nova linha pelo Tratado de Saragoça, em 1529, na região das ilhas Molucas, garantindo possessões na Ásia -conforme os portugueses desejavam -e todos os demais tratados foram anulados.</br>

                                    C) A não aceitação portuguesa gerou, logo a seguir, em 1494, o Tratado de Tordesilhas, que deslocava o meridiano para 370 léguas a oeste de Cabo Verde. Esse território mais largo para os portugueses satisfez seus interesses de controle do Atlântico Sul.</br>

                                    D) Os portugueses acabaram atacando os espanhóis e foram derrotados, o que gerou a chamada União Ibérica.</br>

                                    E) Os portugueses se uniram à Inglaterra e atacaram a Espanha com a chamada “Invencível Armada”.</br>
                                    </br>

                                    </p>

                                    <p><strong>Questão 7:</strong>  O pioneirismo das Grandes Navegações coube aos países ibéricos. Portugal e Espanha, inclusive, chegaram a decidir dividir o mundo entre eles -até mesmo apoiados pelo papa -com os chamados “tratados de rivalidade”.  Contudo, mais adiante, outros países europeus partiram para o avanço paraas terras do Novo Mundo. Dentre eles, estavam holandeses, franceses e ingleses. Quais foram os motivos que fizeram, especialmente, franceses e ingleses terem navegações tardias em relação aos países da Península Ibérica?</br>
                                    </br>

                                    A) O interesse dos franceses e dos ingleses no fomento da agricultura e, posteriormente, da indústria. Por isso, não participaram das Grandes Navegações e ultrapassaram, não muito tempo depois, os países ibéricos em termos de desenvolvimento econômico.</br>

                                    B) A falta de aparato tecnológico dos franceses e dos ingleses, necessário para as navegações de grandes distâncias. Precisavam, assim, comprar tal conhecimento dos portugueses, o que só foi possível no século XVII.</br>

                                    C) O envolvimento dos franceses e dos ingleses na Guerra dos Cem Anos, além de não haver uma burguesia interessada no desenvolvimento marítimo. Assim, centralizaram suas atenções para o comércio europeu e não se preocuparam com o eixo do Atlântico como forma de expandir negócios.</br>

                                    D) Os problemas internos sofridos pelos franceses e ingleses: o primeiro foi o dispendioso e demorado conflito da Guerra dos Cem Anos e, a seguir, as dificuldades mais particulares: no caso dos ingleses, a necessidade de superar um conflito civil (a Guerra das Duas Rosas), e dos franceses, a convivência com sangrentas e terríveis guerras de religião. Esses conflitos internos minaram os esforços de concentrar recursos no Novo Mundo. </br>

                                    E) Os problemas internos sofridos pelos franceses e ingleses, cuja questão para ambos foi a demora em vencer os bárbaros que constantemente faziam incursões e devastavam o sistema produtivo.</br>
                                    </br>

                                    <p>

                                    <p><strong>Questão 8:</strong> A colonização inglesa da América do Norte apresentou diferenciações significativas. Nas áreas do Norte e do Centro, formaram-se as chamadas colônias de povoamento. Já no Sul, o sistema implantado foi o de colônias de exploração. Qual foi a questão natural central que determinou essa diferenciação?</br>
                                    </br>

                                    A) A decisão da Inglaterra em criar uma Nova Inglaterra no Norte para ser extensão dos seus domínios, não tendo, com isso, nenhuma intenção de explorá-los.</br>

                                    B) Os grupos de religiosos do Norte que desejavam criar uma nova pátria e, no Sul, só queriam enriquecer e retornar para a Metrópole.</br>

                                    C) A religiosidade dos ingleses, que abominavam qualquer sistema de exploração. Com isso, sonharam em criar uma nova pátria, mas isso não impediu que usurpadores explorassem o Sul.</br>

                                    D) A luta dos ingleses contra a escravidão no mundo para expandir seu mercado consumidor pelo planeta, já que viveram a Revolução Industrial no século XVI. Assim, as colônias do Norte seriam um exemplo da forçados negros ao mundo. Daí a criação de tanto preconceito racial nos Estados Unidos.</br>

                                    E) A diferenciação climática. O Norte, de clima temperado, não oferecia atrativos diretos parao enriquecimento da Metrópole, pois não era possível criar ali produtos diferentes dos já existentes na Europa. Já o Sul, de clima tropical, possibilitava a exploração.</br>
                                    </br>

                                    </p>

                                    <p><strong>Questão 9:</strong> A prática econômica da colonização espanhola, em boa medida, procurou se aproveitar da estrutura existente criada pelos nativos para que a dominação não se tornasse tão diferente da que já era praticada no local. Um dos exemplos mais evidentes é a construção da Cidade do México sobre as ruínas de Tenochtitlán e da Catedral sobre o principal templo da cidade. Era evidente que o que estava em jogoera a mudança de senhores, mas não se discutia a dominação. Nesse sentido, os espanhóis se aproveitaram também da estrutura de impostos já existente entre os povos dominados como forma central do uso de mão de obra. Que estrutura era essa e como ela se desenvolvia?</br>
                                    </br>

                                    A) O trabalho escravo, já bastante adotado pelos locais, que garantia a exploração máxima da mão de obra e gerava conflitos contínuos no interior do território.</br>

                                    B) O trabalho compulsório, temporário e planejado, conhecido genericamente pelo nome de “repartimiento” e “mita” na área andina, e “cuatequil” entre os astecas. Com o passar do tempo, os indígenas eram cada vez mais explorados e oprimidos. </br>

                                    C) O trabalho servil, em que o indígena deveria trabalhar para seu senhor europeu em troca de sua sobrevivência no território em que já estava.</br>

                                    D) O trabalho assalariado nos moldes dos europeus, que garantia uma atividade econômica para toda a população indígena e, aos poucos, acabou propiciando a formação de um significativo mercado interno.</br>

                                    E) O trabalho servil temporário, em que o indígena deveria trabalhar por sete anos para o senhor que dominava suas terras para, a seguir, garantir sua propriedade.</br>
                                    </br>

                                    </p>

                                    <p><strong>Questão 10:</strong> Uma das características mais específicas da colonização, sobretudo do Norte das Treze Colônias, era o self-government(“governo próprio”). Qual foi a repercussão importante dessaquestão para a formação dos Estados Unidos posteriormente?</br>
                                    </br>

                                    A) O processo de independência, pois aqueles que já desfrutavam da liberdade não desejavam seroprimidos sem ter nenhuma participação na decisão.</br>

                                    B) O ódio dos americanos pela escravidão negra, abolindo, assim, qualquer tipo de preconceito.</br>

                                    C) O desenvolvimento econômico americano, pois, logo no século XVI, já partiram para a Marcha para o Oeste espalhando as ideias de liberdade e democracia.</br>

                                    D) A promoção de ataques, por parte dos americanos, a outros países da América no século XIX.</br>

                                    E) O desemboque na Primeira Guerra Mundial, quando a Alemanha partiu para o ataque à democracia americana.</br>
