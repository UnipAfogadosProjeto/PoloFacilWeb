                                    <p><strong>Questão 3:</strong> "O tempo e o espaço são modos pelos quais pensamos e não condições nas quais vivemos" (Albert Einstein).</br>
                                    </br>

                                    A citação do físico Einstein dialoga com a perspectiva antropológica sobre tempo e espaço. Assinale a seguir a alternativa que, diferente do autor, não dialoga com esta perspectiva.</br>

                                    A) Não há nada na definição de tempo e espaço que possa ser tido como sóciohistóricos para a Antropologia.</br>

                                    B) Espaço é aquilo que organiza a noção de pertencimento a um lugar, a um “grupo” fixado a partir um lugar.</br>

                                    C) Tempo é aquilo que dá sentido ao fluxo da vida, algo que utilizamos como símbolo para lhe dar um começo, meio e fim, uma organização, coerência.</br>

                                    D) A articulação tempo -espaço é o que dá sentido à experiência social individual como parte de uma coletividade.</br>

                                    E) Tempo e espaço são marcadores que localizam o indivíduo como que em relação a outros indivíduosque compartilham destas mesmas verdades que, embora sociais, são tidas como naturais.</br>
                                    </br>

                                    </p>

                                    <p><strong>Questão 4:</strong> Observando o CÓDIGO DE ÉTICA DO ANTROPÓLOGO E DA ANTROPÓLOGA, da Associação Brasileira de Antropologia, assinale a alternativa que representa uma postura de pesquisa ética, partindo deste debate</br>
                                    </br>

                                    A) Ao realizar pesquisa com grupos como traficantes, pessoas que comentem abuso sexual contra menores de idade e outros, é responsabilidade do antropólogo denunciar estas pessoas às autoridades competentes.</br>

                                    B) O código de ética serve para atender ideais corporativistas, ou seja, serve para que profissionais de uma área se protejam mutuamente de profissionais de outras áreas.</br>

                                    C) A discussão acerca da ética na pesquisa antropológica é acessória. Ela se mantém fixa ao longo do tempo. Engloba os interesses do pesquisador e não das comunidades pesquisadas. </br>

                                    D) Ter ética reconhecer que existem normas de condutas que devem ser tomadas com relação a todos os aspectos que envolvem a vida profissional de alguém e, para além de reconhecê-las, praticá-las.</br>

                                    E) É justificável omitir que se está fazendo pesquisa da população pesquisada. Em nome de uma boa pesquisa os fins justificam os meios.</br>
