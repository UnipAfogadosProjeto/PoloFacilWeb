                                    <p><strong>Questão 1:</strong> Sobre a noção de não lugares, proposta por Marc Augé e as críticas recentes a este conceito, é possível afirmar que:</br>
                                    </br>

                                    A) Uma das principais características dos não-lugares, como os aeroportos, é sua característica incontestável de neutralidade e uniformidade.</br>

                                    B) Os não lugares não substituíram e nem se impuseram hegemonicamente à forma espacial e ao conteúdo social dos lugares-lugares.</br>

                                    C) O conceito de não lugar é sustentável apenas naquelas sociedades que vivem em isolamento e ágrafas, pois não possuem história. </br>

                                    D) Os não lugares incorporaram, ao longo do tempo, uma negação absoluta e radical dos lugares tradicionais.</br>

                                    E) Houve, com a globalização, uma erosão inevitável dos lugares em razão da fragmentação dos laçossociais.</br>
                                    </br>

