                                    C) A Antropologia está adequada ao mundo de hoje porque se pauta em um projeto hegemônico, quase totalmente europeu, que por ser erudito teria como compreender melhor as outras culturas. </br>

                                    D) Um/a pesquisador não é apenas “fruto” das aulas que assistiu e das teorias que leu, mas também de todas as os fatos que permearam sua trajetória de vida e de como esses fatos influenciaram sua subjetividade.</br>

