                                    </br>

                                    A) As técnicas corporais é que adaptarão o corpo ao seu emprego na vida social.</br>

                                    B) As técnicas corporais variam de uma sociedade para outra, bem como dentro de uma mesma sociedade com o passar do tempo.</br>  

                                    C) Nosso corpo é determinado pela genética, biologia e atividade neural e os modos como nos movimentamos estão exclusivamente relacionados a eles.</br>

                                    D) O corpo é o objeto e meio técnico do homem para a vida em sociedade.</br>

                                    E) Alguns modos de agir tidos como importantes em dado contexto sociológico pode ser obsoleto em outros.</br>
                                    </br>

