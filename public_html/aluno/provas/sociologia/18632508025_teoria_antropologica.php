                                    C) Classe, raça/etnia, gênero e sexualidades, geração, classe social e religião são marcadores sociais da diferença. Produzem lugares diferentes para cada um de nós na relação com os outros na vida social.</br>

                                    D) As minorias tem dificuldades de acesso aos bens de cidadania porque nos embates sociais não temquantidade numérica o suficiente para garantir que suas reivindicações sejam reconhecidas e respeitadas.</br>

