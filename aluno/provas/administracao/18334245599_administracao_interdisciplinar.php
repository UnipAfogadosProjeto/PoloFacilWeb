                                    <p><strong>Questão 4:</strong> Uma empresa investigava as causas de um problema. Para encontrar o problema, utilizou a ferramenta Brainstorming, com intuito de enumerar todos os problemas existentes no fluxo de informaçõesentre o PPCP e o almoxarifado.</br>
                                    </br>

                                    A ferramenta foi utilizada em reunião com os funcionários responsáveis pelo PPCP e pelo almoxarifado.  Os problemas mencionados na reunião estão expostos abaixo, seguindo primeiramente a fase criativa e, logo a seguir, a fase crítica. Os principais problemas considerados na fase final foram:</br>
                                    </br>

                                    − Saldos fantasmas no inventário do almoxarifado. </br>

                                    − Paradas das linhas de produção por falta de tampas e rolhas.</br>

                                    − Rolhas/tampas de um tipo guardadas em caixas de outro tipo. </br>

                                    − Programação não atendida. − Devoluções não confirmadas. </br>

                                    − Inventário não confiável. </br>

                                    − Atrasos na entrega de itens requisitados na programação.</br>
                                    </br>

