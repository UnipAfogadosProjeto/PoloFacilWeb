                                    <p><strong>Questão 1:</strong> Taylor formulou alguns princípios básicos para a organização, entre eles: a necessidade de se controlar o ritmo de trabalho; a necessidade de se treinar o indivíduo para a execução de uma tarefaindicada pela gerência;  haver na empresa uma padronização do trabalho;  dar incentivos salariais;  haver  planejamento e controle do trabalho; ter especialistas em cada uma das áreas da empresa. Para Taylor, a especialização:</br>
                                    </br>

                                    A) Era uma forma de a empresa ser bem controlada.</br>

                                    B) Intensificava o ritmo de trabalho do operador.</br>

                                    C) Reduzia a necessidade de treinamento do indivíduo para a realização da tarefa.</br>

                                    D) Elimina todos os elementos estranhos à atividade a fim de que a atenção do trabalhador esteja concentrada na tarefa essencial.</br>

                                    E) Permite um melhor planejamento e controle da empresa.</br>
