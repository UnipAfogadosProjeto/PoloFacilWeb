                                    A)  VI -V -II -I -IV -III</br>

                                    B)  III -V -I -VI -IV -II </br>

                                    C)  V -I  -II -III -VI -IV </br>

                                    D)  V -I -III  -IV -II -VI </br> 

                                    E)  III -VI -IV -V -II -I </br>
