                                    <p><strong>Questão 2:</strong> De acordo com Kotler e Armstrong em Princípios de Marketing(1999, p. 285), asempresas estão valorizando cada vez mais a logística por várias razões: primeiro, por ser um elemento essencial no que se refere aos serviços prestados aos clientes, por produzir consideráveis reduções de custos para a empresa e para os consumidores, devido à imensa variedade de produtos criando a necessidade de um aprimorado gerenciamento de logística e, por fim, aprimoramento da tecnologia de informações criando oportunidade de maior eficiência na distribuição. Observe a figura que representa a cadeia de valor de um livro.</br>
                                    
