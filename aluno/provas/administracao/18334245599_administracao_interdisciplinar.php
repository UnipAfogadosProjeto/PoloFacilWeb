                                    <p><strong>Questão 6:</strong> Os acidentes do trabalho ainda constituem importante problema de saúde pública  noBrasil. Dada a gravidade do problema, assume grande importância a detecção dos riscos antes que os acidentes aconteçam. Quando ocorre um acidente é importante identificar suas causas diretas para que acidentes do mesmo tipo não voltem a ocorrer. O método de Árvore de Causas (ADC) é uma técnica de análise do acidente, mas esse método exige reconstrução detalhada e com a maior precisão possível da história do acidente, registrando-seapenas fatos, também denominados fatores de acidente, sem emissão de juízos de valor e sem interpretações, para, retrospectivamente, a partir da lesão sofrida pelo acidentado, identificar a rede de fatores que culminou no acidente de trabalho (AT).O método utiliza o conceito de variação, entendida como mudança ocorrida em relaçãoao funcionamento habitual do sistema (indústria, oficina etc.), considerada indispensável à ocorrência do acidente. </br>
                                    </br>

                                    Utiliza também o conceito de atividade, constituída por alguns componentes, EXCETO: </br>
                                    </br>

                                    A) Indivíduo (I), considerado em seus aspectos físicos e psico-fisiológicos.</br>

                                    B) Tarefa (T), entendida como a sequência de operações executadas pelo indivíduo e passível de observação.</br>

                                    C) Material (M), representado por máquinas, instrumentos, ferramentas, matérias-primas e insumos necessários ao desenvolvimento do trabalho.</br>

                                    D) Meio ambiente (MA) representado por instalações e condições de trabalho externas ao indivíduo (temperatura, umidade e iluminação).</br>

                                    E) Meio de trabalho (MT), entendido em seus aspectos físicos e em suas relações sociais.</br>
                                    </br>

                                    </p>

                                    <p><strong>Questão 7:</strong> A Escola Fundamentalista aprimorou diversos métodos e indicadores que se tornaram clássicos para a avaliação de empresas. Eis alguns:</br>
                                    </br>

                                    1. Índice Preço-Lucro,  PL, que decorre da divisão entre o preço de mercado de um determinado papel, em certo momento, pelo lucro líquido anualizado da ação.</br>

                                    2. Cash-yield: relação do dividendo anualizado pelo preço de mercado da ação. Este é o método que a maior parte dos investidores, com horizonte de longo prazo, adota. </br>

                                    3. Cotação/Valor patrimonial: quociente entre o preço de mercado da ação e o valor patrimonial da mesma. </br>

                                    4. Índice Sharpe: mede o grau de risco de uma carteira em relação à sua rentabilidade. O índice é calculado pela diferença entre a rentabilidade da carteira Rc e do ativo Ri utilizado como comparação, dividido pelo desvio padrão da carteira. O ativo i pode ser um certificado de depósito bancário (CDB-over) ou semelhante.</br>

                                    5. Capital Asset Pricing Model: que relaciona os preços das ações com taxas de retorno, determinando uma linha característica para o título i, genericamente na forma Ri= bi Rm, onde Ri, o retorno excessivo esperado no título genérico i, pode ser calculado pelo produto de bi pelo do título i pelo Rm retorno excessivoesperado na carteira de mercado. </br>
                                    </br>

                                    Admita-se uma ação que está ao preço de $100 e da qual se espera um dividendo de $12 no presente exercício. O patrimônio líquido é de $90 milhões e a empresa tem 1.000.000 de ações.</br>
                                    </br> 

                                    Com base no texto acima, pode-se afirmar: </br>
                                    </br>

                                    A) O Índice Preço-Lucro é de $0,90.</br>

                                    B) O Cash-yieldé de 12%.</br>

                                    C) A Cotação/Valor patrimonial é de 0,90.</br>

                                    D) O Índice Sharpe é de 90.</br>

                                    E) O Índice Preço-Lucro é de 1,1.</br>
                                    </br>

                                    <p>

                                    <p><strong>Questão 8:</strong> O passivo ambiental representa o sacrifício de benefícios econômicos que será realizado para a preservação, recuperação e proteção do meio ambiente, de forma a permitir a compatibilidade entre o desenvolvimento econômico e o meio ecológico ou em decorrência de conduta inadequada em relação às questões ambientais. Para a identificação do passivo ambiental das empresas, entre as formas possíveis, estaria o uso das informações contidas tanto no EIA quanto no RIMA. Leia o que se explica por EIA e por RIMA:</br>
                                    </br>

                                    EIA -Estudo de Impacto Ambiental: pode ser exigido no licenciamento de projetos de empreendimentos e atividades considerados efetivos ou potencialmente causadores de significativa degradação do meio ambiente. </br>
                                    </br>

                                    RIMA -Relatório de Impacto ao Meio Ambiente: deve refletir as conclusões do EIA, contendo, entre outros aspectos, os objetivos e as justificativas do projeto, as alternativas tecnológicas e locacionais, uma síntesedos resultados dos estudos de diagnósticos ambientais; a descrição dos prováveis impactos ambientais da implantação e operação.</br>
                                    </br>

                                    Com base no texto acima, aponte a alternativa FALSA:</br>
                                    </br>

                                    A) O EIA  pode ser exigido no licenciamento de atividades que têm o potencial de degradar o meio ambiente.</br>

                                    B) O passivo ambiental exprime os custos econômicos para a preservação, recuperação e proteção do meio ambiente.</br>

                                    C) O EIA e o RIMA servem para identificar o passivo ambiental das empresas.</br>

                                    D) O RIMA , entre outras coisas, apresenta a descrição dos prováveis impactos ambientais da implantação e operação de alguma atividade.</br>

                                    E) O RIMA é independente do EIA.</br>
                                    </br>

                                    </p>

                                    <p><strong>Questão 9:</strong> Há diferença entre eficiência e eficácia. Podemos definir eficiência como a relaçãoexistente entre o resultado obtido e os recursos consumidos para conseguir aquele resultado. Eficiência trata de como fazer, não do que fazer. Outro conceito é o de eficácia: ser eficaz é atingir os objetivos propostos. A eficácia está ligada ao objetivo em si. Quando ao mesmo tempo se é eficaz (se atinge a meta) e se é eficiente (faz-se uma boa utilização dos recursos) dizemos que se é efetivo.</br>
                                    

                                    </br><img src="./img/18334245599_administracao_interdisciplinar_img3.jpg"></br>
                                    </br>

                                    Com base no texto e nos casos da figura acima, pode-se afirmar:</br>
                                    </br>

                                    I-Os casos 1 e 2 são ineficazes, pois não alcançam o objetivo.</br>

                                    II-Os casos 3 e 4 são eficazes, pois alcançam o objetivo.</br>

                                    III-O caso 4 é eficaz, mas não é eficiente.</br>

                                    IV-O caso 2 é ineficaz, mas eficiente.</br>

                                    V-O caso 3 é efetivo.</br>
                                    </br>

                                    Estão corretas as afirmações:</br>
                                    </br>

                                    A) I e II.</br>

                                    B) III e IV.</br>

                                    C) I, II, III e V.</br>

                                    D) I, II, III e IV.</br>

                                    E) I, II, III, IV e V.</br>
                                    </br>

                                    </p>

                                    <p><strong>Questão 10:</strong> O modelo Racional de Tomada de Decisão é usado geralmente por organizações que pesam suas opções e calculam níveis de risco ótimos. Assim, acreditam ser possível minimizar a incerteza, possibilitando uma decisão que assegura o sucesso da ação com efeitos duradouros, dizem Stoner e Freeman em Administração, PHB(1999).O modelo racional exige do decisor um conhecimento claro a respeito de seu sistema de preferênciase suas alternativas, sobre o comportamento dos elementos do universo em questão, de uma forma de medir estes elementos, de uma metodologia suficientemente rigorosa para chegar a apreciações razoavelmente aceitáveis e de uma quantidade adicional de variáveis, elementos e métodos para esta medição. </br>
                                    </br>

