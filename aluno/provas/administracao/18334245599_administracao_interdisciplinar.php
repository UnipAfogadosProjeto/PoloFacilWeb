                                    </br>

                                    I -Atribuir os pesos corretos a esses critérios </br>

                                    II -Escolher a melhor opção </br>

                                    III -Definir o problema </br>

                                    IV -Avaliar as alternativas</br> 

                                    V -Identificar os critérios de decisão </br>

                                    VI -Desenvolver alternativas </br>
