                                    </br>

                                    I -Apenas os alunos não alfabetizados pertencentes às classes econômicas mais favorecidas têm o direito de frequentar esse nível de ensino.</br>

                                    II -A Educação de Jovens e Adultos é retratada nas normas legislativas sob a ótica de reformas que priorizam a Educação Fundamental das crianças em detrimento dos outros níveis e grupos sociais.</br>

                                    III -Na legislação, nenhuma palavra foi dita sobre o analfabetismo. Nesse sentido, fecham-se os olhos para o enorme contingente de pessoas jovens e adultas que não tem o domínio da leitura, da escrita e do cálculo.</br>

                                    IV -A forma de organização dos sistemas que atuam com a Educação de Jovens e Adultos, conforme a Lei 9.394/96, deve ser focada em questões relativas às práticas sociais e ao mundo do trabalho.</br>

                                    V -A Educação de Jovens e Adultos é retratada nas normas legislativas como uma modalidade de ensino que deve ser frequentada por aqueles que têm condições econômicas de financiar esse tipo de educação.</br>
