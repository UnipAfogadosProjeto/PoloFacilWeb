                                    <p><strong>Questão 1:</strong> A LDB 9.394/96 estabelece que:</br>
                                    </br>

                                    “Art. 38. Os sistemas de ensino manterão cursos e exames supletivos, que compreenderão a base nacional comum do currículo, habilitando ao prosseguimento de estudos em caráter regular.1Os exames a que se refere este artigo realizar-se-ão:I -no nível de conclusão do ensino fundamental, para os maiores de quinze anos;II -no nível de conclusão do ensino médio, para os maiores de dezoito anos.2Os conhecimentos e habilidades adquiridos pelos educandos por meios informais serão aferidos e reconhecidos mediante exames.”</br>
                                    </br>

                                    Conforme verificado, a LDB 9.394/96 propõe um rebaixamento de idade para prestar os exames no Ensino Fundamental e Médio. Sérgio Haddad argumenta que essa medida terá algumas consequências. Com relação a isso, assinale a alternativa correta:</br>
                                    </br>

                                    A) Extinção com relativa rapidez do número de pessoas analfabetas em nosso país.</br>

                                    B) Aceleração dos estudos para aqueles que não tiveram oportunidade em idade própria, possibilitando a essa clientela as mesmas condições de acesso ao Ensino Superior.</br>

                                    C) O rebaixamento da idade mínima para os exames supletivos representa um instrumento de democratização do ensino.</br>

                                    D) Essas determinações podem ter uma abrangência nula no sistema, uma vez que seus resultados não modificarão a forma como esse nível de ensino vem se materializando na prática escolar e educacional.</br>

                                    E) Os estudiosos do ensino supletivo são contrários a essa determinação porque, considerando os padrões atuais de rendimento escolar no ensino regular -com elevados índices de repetência, sucessivas evasões e rematrículas -, é muito provável que o rebaixamento da idade mínima para os exames supletivos represente menos um instrumento de democratização de oportunidades educacionais e mais um mecanismo de regularização do fluxo escolar e aceleração dos estudos. </br>
                                    
                                    </br>

                                    </p>

