                                    II -A prática pedagógica a ser trabalhada em uma sala de Educação de Jovens e Adultos deve envolver o conteúdo que apresenta características adequadas às necessidades dos que frequentam esta modalidade de ensinosem descuidar do conhecimento científico, e por isso atende as determinações legais.</br>

                                    III -São adequados para serem trabalhados com uma sala de Educação de Jovens e Adultos textos cujas informações não estejam em conformidade com as vivências cotidianas e sociais dos alunos, uma exigência legal. </br>

                                    IV -O texto a ser trabalhado com os alunos de Educação de Jovens e Adultos, conforme a legislaçãoestabelece, deve estar estritamente vinculado às diretrizes curriculares desenvolvidas com os alunos em idade escolar que frequentam o Ensino Fundamental.</br>
