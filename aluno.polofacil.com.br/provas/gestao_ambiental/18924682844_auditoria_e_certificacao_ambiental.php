                                    <h4>Questões de múltipla escolha </h4>

                                    <p>Disciplina: 684730 - Auditoria e Certificação Ambiental</p>

                                    <p><strong>Questão 1:</strong> Como última etapa da auditoria, a reunião de encerramento é o último ato realizado nas dependências da organização auditada, ocorrendo para apresentação de resultados e para coletar as últimas considerações, que deverão compor o relatório final a ser emitido. Com relação a esse aspecto, assinale a alternativa correta:</br>
                                    </br>

                                    A) A equipe de auditados terá um papel importante nesta reunião, devendo estar bem preparada para sistematizar informações, bem como as constatações da auditoria, originadas das evidencias coletadas.</br>

                                    B) Deve-se tomar todo o cuidado para que a reunião ocorra na mais incompleta harmonia.</br>

                                    C) Na reunião a empresa auditada e seus representantes, membros de sua equipe, ficarão sabendopor completo das constatações da auditoria.</br>

                                    D) Sempre é fácil para os membros da equipe da empresa auditada entenderem as não conformidades identificadas.</br>

                                    E) A reunião de encerramento não precisa ser conduzida pelo auditor líder, que será responsável por apresentar os resultados.</br>
                                    </br>

                                    </p>

                                    <p><strong>Questão 2:</strong> A aplicação da auditoria ambiental no local tem seu início com uma reunião de abertura, para que os auditores se apresentem à direção dos auditados ou, quando for o caso, aos responsáveis pelas funções ou processos a serem auditados. Entre os propósitos desta reunião, além de estabelecer o cenário geral da auditoria, constam a apresentação do escopo e dos objetivos, a confirmação do plano de auditoria e um resumo dos métodos por meio dos quais ele será executado, além da confirmação dos canais de comunicação, dos recursos disponíveis e a oportunidade de esclarecimento de eventuais dúvidas por parte dos auditados. A lguns pontos podem nortear a reunião de abertura, que deve ser conduzida pelo auditor líder. </br>
                                    </br>

                                    Assim, assinale a alternativa correta:</br>
                                    </br>

                                    A) Confirmação do escopo e objetivo da auditoria: o tom autoritário deve ser evitado. Antes deiniciar a reunião, o auditor líder deve deixar o auditado mais à vontade, criando um clima de descontração, uma vez que pode ocorrer deste estar muito ansioso, fator que pode comprometer a auditoria.</br>

                                    B) Apresentação da equipe de auditores: após a apresentação do auditor líder, cada um dos demais membros da equipe de auditoria se apresenta, estabelecendo uma relação cordial com o auditado e, então, é explicado o que será auditado.</br>

                                    C) Descontração: deve ser exposta pelo auditor líder, juntamente com os critérios sob os quaisserá realizada a auditoria, como normas, leis ou outros.</br>

                                    D) Confirmação do plano de auditoria : o auditor líder deve esclarecer que métodos serão utilizados na coleta de evidências, para caracterização de conformidades e não-conformidades, como entrevistas, verificação de documentos e registros, coletas de amostras, entre outros.</br>

                                    E) Métodos a serem utilizados na auditoria : durante a apresentação do plano, o qual deve ter flexibilidade para possíveis alterações em função do andamento da auditoria (conforme já esclarecido na etapa de preparação), o auditor líder deve dar a oportunidade ao auditado de esclarecer eventuais questionamentos e dúvidas.</br>
                                    
                                    </br>
                                    </p>

                                    <p><strong>Questão 3:</strong> Muitos benefícios podem emergir por meio da  prática das auditorias. Com relação a esses benefícios, assinale a alternativa correta:</br>
                                    </br>

                                    A) Falta de estabelecimento estimativo dos passivos ambientais, existentes e potenciais, de acordo com determinações legais em vigor.</br>

                                    B) Promoção da aproximação entre empresa e órgãos públicos responsáveis pelo controle ambiental, fazendo-os agir em parceria.</br>

                                    C) Promoção da aproximação entre empresa e comunidades circundantes do empreendimento e/ou partes interessadas, estabelecendo agendas incomuns.</br>

                                    D) Falta de harmonização de práticas e procedimentos nas diversas unidades operacionais de umaorganização.</br>

                                    E) Não estabelecimento de prioridade nas áreas a investir, priorizando as irregularidades maisgraves.</br>
                                    </br>

                                    </p>

                                    <p><strong>Questão 4:</strong> A auditoria ambiental é uma importante ferramenta de gestão ambiental. Se ela é conduzida de maneira experiente, completa e adequada, pode revelar a saúde ambiental da organização, trazendo vantagens. </br>
                                    </br>

                                    Em relação às vantagens, assinale a alternativa correta:</br>
                                    </br>

                                    A) Detecção de conformidades e não conformidades em relação à legislação, a regulamentos ou normas e em relação à política ambiental da organização, caso existente. Isto gera a oportunidade de melhorias no desempenho ambiental, a partir da execução de ações corretivas direcionadas.</br>

                                    B) Necessidade de destinação de recursos adicionais para o programa de auditoria ambiental.</br>

                                    C) Auxílio na maximização de resíduos gerados e dos recursos utilizados pela organização, gerando desperdícios e gastos desnecessários.D) Possibilidade de destinação inesperada e expressiva de recursos para a correção de não conformidades detectadas na auditoria ambiental.</br>

                                    E) Possibilidade da ocorrência de pressões de órgãos governamentais e de grupos ambientais para que a organização mostre os resultados da auditoria ambiental, o que, em muitos casos (quando muitas não conformidades são detectadas pela auditoria), pode abalar a credibilidade da organização junto ao público.</br>
                                    </br>

                                    </p>

                                    <p><strong>Questão 5:</strong> Quando se aprofunda a análise da NBR ISO 19011:2012, existem evidências sobre a abordagem que a norma faz sobre os princípios de auditoria. Com relação a esse aspecto, assinale a alternativacorreta:</br>
                                    </br>

                                    A) Profissionalismo: estabelecer confiança na realização da auditoria.</br>

                                    B) Apresentação justa: obrigação de reportar as constatações e conclusões com veracidade e exatidão.</br>

                                    C) Conduta Ética: total compreensão e reconhecimento da importância de sua tarefa.</br>

                                    D) Apresentação justa: o processo de auditoria e os auditores que a compõem devem ser independentes das atividades a serem auditadas e isentos de tendências pré-estabelecidas e conflitos de interesse.</br>

                                    E) Independência: determinar ações com base em evidências verificáveis.</br>
                                    </br>

                                    </p>

                                    <p><strong>Questão 6:</strong> Independência é um dos princípios que fundamentam a busca pela eficácia e confiabilidade da auditoria para os fins a que se destina e que devem ser seguidos por auditores. Ela é considerada a base para a imparcialidade da auditoria e para a objetividade de suas conclusões. Auditores devem ser independentes da atividade a ser auditada, para que sejam evitados conflitos de interesse e comportamento tendencioso. Para que as constatações e conclusões da auditoria sejam baseadas somente em evidências, os auditores devem manter a objetividade durante todo o processo de auditoria. Além da independência, uma característica pessoal indispensável a um auditor é:</br>
                                    </br>

                                    A) Integralidade.</br>

                                    B) Conduta ética.</br>

                                    C) Universalidade.</br>

                                    D) Desorganização pessoal.</br>

                                    E) Falta de controle.</br>
                                    </br>

                                    </p>

                                    <p><strong>Questão 7:</strong> As auditorias de SGA (Sistema de Gestão Ambiental) podem ser de pré-certificação, de certificação, de manutenção ou de recertificação. Com relação a esse assunto, avalie as afirmativas a seguir:</br>
                                    </br>

                                    I -A auditoria de pré-certificação ou inicial é realizada antes da auditoria de certificação, para identificar se existem ajustes no sistema a serem providenciados, não sendo obrigatória no processo de certificação.</br>

                                    II -A auditoria de certificação é obrigatória no processo de certificação de um SGA. A partir do resultado desta auditoria, a certificação do sistema é recomendada ou não, pois por meio dela é avaliada a conformidade da empresa com princípios estabelecidos nas normas pelas quais ela pretende se certificar.</br>

                                    III -A auditoria de recertificação não é obrigatória no processo de certificação de um SGA, mas érecomendado que seja realizada de seis em seis meses ou pelo menos uma vez ao ano, no período compreendido entre a auditoria de certificação e a auditoria de recertificação.</br>

                                    IV -A auditoria de manutenção é realizada, geralmente, dois ou três anos após a ocorrência da auditoria de certificação. Seu nível de exigência é superior às auditorias anteriores, já que se espera que após esse tempo o SGA esteja consolidado.</br>
                                    </br>

                                    Estão corretas as afirmativas:</br>
                                    </br>

                                    A) I e II, apenas.</br>

                                    B) II e IV, apenas.</br>

                                    C) I, II e III, apenas.</br>

                                    D) II, III e IV, apenas.</br>

                                    E) I, II, III e IV.</br>
                                    </br>

                                    <p>

                                    <p><strong>Questão 8:</strong>  A palavra auditoria vem do latim auditore(aquele que ouve) e, como disciplina, surgiu no setor financeiro para a verificação sistemática da adequação da contabilidade empresarial às exigências legais e normas estabelecidas. Dessa forma, com base nesse contexto, assinale a alternativa correta em relação à auditoria:</br>
                                    </br>

                                    A) É um processo sistemático e não documentado de verificação.</br>

                                    B) É um processo executado para obter e avaliar evidências de forma não objetiva.</br>

                                    C) É um instrumento de verificação que se destaca como importante ferramenta de gestão ambiental.</br>

                                    D) Por meio da auditoria ambiental, não é possível a identificação dos pontos “fracos” e dos pontos “fortes” da organização frente às questões do meio ambiente.</br>

                                    E) É um instrumento de verificação que não se destaca na gestão ambiental.</br>
                                    </br>

                                    </p>

                                    <p><strong>Questão 9:</strong> Com relação ao escopo de uma auditoria, fixa-se que devem ficar esclarecidas algumas informações necessárias para compreender o ambiente e as condições em que a auditoria será realizada. </br>
                                    </br>

                                    Avalie as afirmativas a seguir:</br>
                                    </br>

                                    I -Local: cidade, estado, país, em qual unidade, mais especificamente, se a unidade encontra-se em área rural ou urbana, se há algum regime especial que recaia sobre o local da realização da auditoria.</br>

                                    II -Enquadramento organizacional: sob a responsabilidade de qual área ou áreas funcionais da organização a unidade auditada está vinculada (produção, estação de tratamento, armazenamento, logística etc.), sob quais condições e com quem a auditoria vai se relacionar diretamente.</br>

                                    III -Objeto: qual será o foco da auditoria, o que será auditado, dependendo da definição do objetivo, para que o objeto também seja definido. </br>

                                    IV -Lapso temporal: período, ou quanto tempo será necessário para que a auditoria seja realizada na unidade auditada. Este item do escopo também dependerá da complexidade definida nos objetivos da auditoria.</br>

                                    V -Especialidade ambiental: qual tema ambiental será averiguado, quais os itens ambientais serão avaliados, quais os impactos serão analisados e mensurados.</br>
                                    </br>

                                    È correto o que se afirma em:</br>
                                    </br>

                                    A) I e II, apenas.</br>

                                    B) II e III, apenas.</br>

                                    C) II e V, apenas.</br>

                                    D) III, IV e V, apenas.</br>

                                    E) I, II, III, IV e V.</br>
                                    </br>

                                    </p>

                                    <p><strong>Questão 10:</strong> Embora também seja uma preocupação da auditoria ambiental verificar se a legislação está sendo cumprida, o seu cumprimento é uma obrigação imposta a todos pelo Estado. Em relação à legislação, assinale a alternativa correta:</br>
                                    </br>

                                    A) A verificação da legislação ambiental pela auditoria não ocorre com o intuito de dominar a legislação existente.</br>

                                    B) Nas auditorias de desempenho ambiental, o atendimento à legislação aplicável representa o maior nível de desempenho ambiental possível em uma organização produtiva.</br>

                                    C) A organização que tenha por objetivo uma auditoria ambiental a fim de obter, por exemplo, acertificação do Sistema de Gestão Ambiental (SGA), não necessitará do estudo e compreensão sobre todas as dimensões relacionadas às normas aplicáveis para atingir tal certificação.</br>

                                    D) Quando se trata da aplicação de normas de certificação por meio de auditorias ambientais, pretende-se que organização atinja sempre o máximo do desempenho ambiental, para além de qualquer expectativa legal.</br>

                                    E) Para obter a certificação do Sistema de Gestão Empresarial (SGA), a empresa não necessitaráde estudo.</br>
